package com.cl.ysyd.common.constants;


public class TokenInfo {
    private Long date;
    private String token;

    public Long getDate() {
        return date;
    }

    public void setDate(Long date) {
        this.date = date;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
